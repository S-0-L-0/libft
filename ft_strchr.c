#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i])
	{
		while (s[i] == (char) c)
			return (i);
		i ++;
	}
	if (c == 0)
		return (i);
	return (0);
}
