#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i])
		i ++;
	if (c == 0)
		return (i);
	while (i -- >= 0)
	{
		while (s[i] == (char) c)
			return (i);
	}
	return (0);
}
